package operatorsUserDefinedMethods;

public class ArithmaticOperators {
	
	public static int add(int a,int b)
	{
		int c=a+b;
		
		return c;
	}
	public static int add(int a,int b,int d)
	{
		int c=a+b+d;
		
		return c;
	}
	
	public  int add(int a,int b,int d,int e)
	{
		int c=a+b+d+e;
		
		return c;
	}
	
	public int sub(int a,int b)
	{
		int c=a-b;
		
		return c;
	}
	

}
